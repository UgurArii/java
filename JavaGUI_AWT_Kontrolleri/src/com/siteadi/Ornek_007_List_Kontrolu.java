        private void list2İtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_list2İtemStateChanged
               int secilenRenk = list2.getSelectedIndex();
	       list2.setForeground(Color.BLACK);
	       if(secilenRenk==0){
		       list2.setBackground(Color.red);
	       }
	       
	        if(secilenRenk==1){
		       list2.setBackground(Color.PINK);
	       }
		
		 if(secilenRenk==2){
		       list2.setBackground(Color.BLUE);
	       }
		
		  if(secilenRenk==3){
		       list2.setBackground(Color.CYAN);
	       }
		   if(secilenRenk==4){
		       list2.setBackground(Color.GREEN);
	       }
		    if(secilenRenk==5){
		       list2.setBackground(Color.YELLOW);
	       }
		    
		     if(secilenRenk==6){
		       list2.setBackground(Color.WHITE);
	       }
		     
		      if(secilenRenk==7){
		       list2.setBackground(Color.GRAY);
	       }
		       if(secilenRenk==8){
		       list2.setBackground(Color.BLACK);
		       list2.setForeground(Color.WHITE);
	       }
        }//GEN-LAST:event_list2İtemStateChanged

