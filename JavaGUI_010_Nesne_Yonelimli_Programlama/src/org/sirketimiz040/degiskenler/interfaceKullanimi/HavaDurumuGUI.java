	public String havaDurumunuTahminEt(){
		 Random random = new Random();
	       int ihtimal = (int)(100*random.nextFloat());
	       
	       String havaDurumu = GUNESLI;
	       
	       if(ihtimal<10){
		       havaDurumu="Karlı";
	       }else if(ihtimal<25){
		       havaDurumu = "Yağmurlu";
	       }else if(ihtimal<30){
		       havaDurumu="Sis";
	       }else if(ihtimal<40){
		       havaDurumu="Bulutlu";
	       }else if(ihtimal<50){
		       havaDurumu="Güneşli";
	       }
	       return havaDurumu;
	}
	
